// ============================================================================
// HELPER: Check if a character is a Bengali consonant
// ============================================================================

export function isConsonant(char: string): boolean {
  const code = char.charCodeAt(0);
  return code >= 0x0995 && code <= 0x09B9; // ক to হ
}

// ============================================================================
// HELPER: Check if a character is a Bengali kar (vowel sign)
// ============================================================================

export function isKar(char: string): boolean {
  const kars = 'ািীুূৃেৈৗ';
  return kars.includes(char);
}

// ============================================================================
// HELPER: Check if a character is a pre-kar (appears before consonant in visual)
// ============================================================================

export function isPreKar(char: string): boolean {
  return 'িেৈ'.includes(char);
}

// ============================================================================
// HELPER: Check if a character is a post-kar (appears after consonant)
// ============================================================================

export function isPostKar(char: string): boolean {
  return 'াীুূৃৗ'.includes(char);
}

// ============================================================================
// HELPER: Check if a character is a nukta/chandrabindu
// ============================================================================

export function isNukta(char: string): boolean {
  return char === 'ঁ';
}
